using namespace std;

int getShipCapasity(vector<int> arr){
    int max = 0;
    for(int i = 0; i < arr.size(); i++){
        if(arr[i] > max) max = arr[i];
    }
    return max;
}

int shippingDaysRequired(vector<int> arr, int capasity){
    long long shipLoad = 0;
    int daysReq = 1;
    for(long long i = 0; i < arr.size(); i++){
        if(shipLoad + arr[i] > capasity){
            daysReq++;
            shipLoad = arr[i];
        }else{
            shipLoad += arr[i];
        }
    }
    return daysReq;
}

int leastCapasityToShipPackagesInDDays(vector<int> arr, int days){
    long long low = getShipCapasity(arr);
    long long high = ((low * (low + 1) / 2));
    while(low <= high){
        long long mid = (low + high) / 2;
        int shippingDays = shippingDaysRequired(arr, mid);
        if(shippingDays <= days){
            high = mid - 1;
        }else{
            low = mid + 1;
        }
    }
    return low;
}

